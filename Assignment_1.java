package assignment;

public class Assignment_1 {

	public static void main(String[] args) {
		
		        // Step 1: Float value
		        float marks = 87.6f;
		        
		        // Step 2: Convert float to double (upcasting)
		        double doubleMarks = marks;
		        
		        // Step 3: Convert double to int (downcasting)
		        int intMarks = (int) doubleMarks;
		        
		        // Step 4: Convert int to char (ASCII conversion)
		        char charValue = (char) intMarks;
		        
		        
		        System.out.println("Float value: " + marks);
		        System.out.println("Double value: " + doubleMarks);
		        System.out.println("Integer value: " + intMarks);
		        System.out.println("Character value: " + charValue);
		    }
		

	}


